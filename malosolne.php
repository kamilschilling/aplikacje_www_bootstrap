            <h2 class="display-3 text-md-center">Ogórki małosolne</h2>
            <p>Ogórki małosolne robi się najczęściej od czerwca do sierpnia. Jak Polska długa i szeroka, do ogórków małosolnych dodaje się różne dodatki. Najpopularniejsze to chrzan, koper i czosnek, ale są też mniej oczywiste, jak np. liść porzeczki, wiśni czy dębu. Co jeszcze warto dodać do ogórków małosolnych, aby były smaczne? Ogórki małosolne to bardzo smaczny i zdrowy dodatek do dań i kanapek dostępny za niewielkie pieniądze. Co ważne, smak ogórków może być za każdym razem inny, w zależności od tego, jakie składniki do nich dodamy, w jakiej proporcji i jak długo będziemy je kisić. Sezon na ogórki małosolne rozpoczyna się już w czerwcu, ale trwa w najlepsze również w lipcu.</p>
